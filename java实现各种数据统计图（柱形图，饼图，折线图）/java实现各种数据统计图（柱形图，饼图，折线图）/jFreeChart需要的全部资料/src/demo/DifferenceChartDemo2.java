    private static JFreeChart createChart(XYDataset xydataset)
    {
        JFreeChart jfreechart = ChartFactory.createTimeSeriesChart("Daylight Hours - London, UK", "Date", "Time", xydataset, true, true, false);
        jfreechart.setBackgroundPaint(Color.white);
        jfreechart.addSubtitle(new TextTitle("Data source: http://www.sunrisesunset.com/", new Font("SansSerif", 0, 12)));
        XYDifferenceRenderer xydifferencerenderer = new XYDifferenceRenderer(Color.blue, Color.blue, false);
        xydifferencerenderer.setRoundXCoordinates(true);
        xydifferencerenderer.setBaseStroke(new BasicStroke(2.0F));
        xydifferencerenderer.setSeriesPaint(0, Color.yellow);
        xydifferencerenderer.setSeriesPaint(1, Color.red);
        xydifferencerenderer.setBaseToolTipGenerator(new StandardXYToolTipGenerator("{0} : {1} = {2}", new SimpleDateFormat("d-MMM-yyyy"), new SimpleDateFormat("kk:mm")));
        XYPlot xyplot = (XYPlot)jfreechart.getPlot();
        xyplot.setRenderer(xydifferencerenderer);
        xyplot.setBackgroundPaint(Color.lightGray);
        xyplot.setDomainGridlinePaint(Color.white);
        xyplot.setRangeGridlinePaint(Color.white);
        xyplot.setAxisOffset(new RectangleInsets(5D, 5D, 5D, 5D));
        xyplot.setDomainCrosshairVisible(true);
        xyplot.setRangeCrosshairVisible(true);
        DateAxis dateaxis = new DateAxis("Time");
        dateaxis.setTickMarkPosition(DateTickMarkPosition.MIDDLE);
        dateaxis.setLowerMargin(0.0D);
        dateaxis.setUpperMargin(0.0D);
        xyplot.setDomainAxis(dateaxis);
        xyplot.setForegroundAlpha(0.5F);
        GradientPaint gradientpaint = new GradientPaint(0.0F, 0.0F, Color.red, 1.0F, 1.0F, Color.orange);
        IntervalMarker intervalmarker = new IntervalMarker((new Day(28, 3, 2004)).getFirstMillisecond(), (new Day(30, 10, 2004)).getFirstMillisecond(), gradientpaint, new BasicStroke(2.0F), null, null, 1.0F);
        intervalmarker.setLabel("British Summer Time");
        intervalmarker.setLabelAnchor(RectangleAnchor.BOTTOM_RIGHT);
        intervalmarker.setLabelFont(new Font("SansSerif", 3, 10));
        intervalmarker.setLabelTextAnchor(TextAnchor.BASELINE_RIGHT);
        intervalmarker.setGradientPaintTransformer(new StandardGradientPaintTransformer(GradientPaintTransformType.HORIZONTAL));
        xyplot.addDomainMarker(intervalmarker, Layer.BACKGROUND);
        DateAxis dateaxis1 = new DateAxis("Time");
        dateaxis1.setLowerMargin(0.14999999999999999D);
        dateaxis1.setUpperMargin(0.14999999999999999D);
        xyplot.setRangeAxis(dateaxis1);
        return jfreechart;
    }

    public static JPanel createDemoPanel()
    {
        JFreeChart jfreechart = createChart(createDataset());
        return new ChartPanel(jfreechart);
    }

