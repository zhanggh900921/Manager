        xyplot.setRangeGridlineStroke(new BasicStroke(1.0F));
        NumberAxis numberaxis = (NumberAxis)xyplot.getRangeAxis();
        numberaxis.setTickUnit(new NumberTickUnit(50000D));
        DateAxis dateaxis = (DateAxis)xyplot.getDomainAxis();
